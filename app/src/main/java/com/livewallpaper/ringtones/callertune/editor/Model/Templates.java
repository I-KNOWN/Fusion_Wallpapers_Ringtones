package com.livewallpaper.ringtones.callertune.editor.Model;

public class Templates {
    private String imageUrl;

    public int img;

    public Templates() {
    }

    public Templates(int img) {
        this.img = img;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
